#!/usr/bin/env python3
"""Generate docs/local-build/EVAL-REPORT.md (Phase 7 before/after report).

"Before" is derived from the Phase 0 targets in ``baseline.json`` (tokens/sec
mid-point of each tier's target range, free-VRAM target). "After" is loaded from
a results JSON produced by ``scripts/run_local_eval.py`` on the rig, if present;
otherwise the after column is rendered as pending. Regenerate after a rig run:

    uv run python scripts/gen_eval_report.py --after docs/local-build/after.json
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

from openjarvis.evals.local_build.report import render_before_after

_ROOT = Path(__file__).resolve().parents[1]


def _toks_mid(rng: str) -> float:
    """Midpoint of a '50-90' style tok/s target string."""
    try:
        lo, hi = (float(x) for x in rng.replace(" ", "").split("-"))
        return round((lo + hi) / 2, 1)
    except Exception:
        return 0.0


def baseline_to_aggregate(baseline: dict) -> dict:
    """Map baseline.json targets into the eval aggregate shape (before column)."""
    targets = baseline.get("targets_to_remeasure_on_rig", {})
    tier_map = {"fast": None, "balanced": None, "deep": None}
    for t in targets.get("tiers", []):
        name = t.get("tier")
        if name in tier_map:
            tier_map[name] = {
                "count": 0,
                "task_success": None,  # not measured at Phase 0
                "tok_per_s": _toks_mid(t.get("tok_s_target", "")),
                "latency_s": None,
                "energy_j": None,
                "free_vram_gb": t.get("free_vram_gb_target"),
            }
    tiers = {k: v for k, v in tier_map.items() if v is not None}
    # Overall = mean of available tier tok/s + free VRAM.
    toks = [v["tok_per_s"] for v in tiers.values() if v["tok_per_s"]]
    free = [v["free_vram_gb"] for v in tiers.values() if v["free_vram_gb"]]
    overall = {
        "count": 0,
        "task_success": None,
        "tok_per_s": round(sum(toks) / len(toks), 1) if toks else 0.0,
        "latency_s": None,
        "energy_j": None,
        "free_vram_gb": round(sum(free) / len(free), 1) if free else None,
    }
    return {
        "label": "baseline-targets",
        "overall": overall,
        "tiers": tiers,
        "categories": {},
    }


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--baseline", default=str(_ROOT / "docs/local-build/baseline.json"))
    ap.add_argument("--after", default=None, help="after.json from run_local_eval.py")
    ap.add_argument("--output", default=str(_ROOT / "docs/local-build/EVAL-REPORT.md"))
    args = ap.parse_args()

    baseline = json.loads(Path(args.baseline).read_text())
    before = baseline_to_aggregate(baseline)

    after = None
    if args.after and Path(args.after).exists():
        after_doc = json.loads(Path(args.after).read_text())
        after = after_doc.get("aggregate", after_doc)

    note = (
        "Generated by `scripts/gen_eval_report.py`. The eval suite is "
        "`openjarvis.evals.local_build.suite` (~50 personal-assistant prompts: "
        "chat, reasoning, tool-use, RAG). Before = Phase 0 targets from "
        "`baseline.json`; after = `scripts/run_local_eval.py` on the rig."
    )
    md = render_before_after(
        before, after, title="Local-Build Eval — Before / After", note=note
    )
    Path(args.output).write_text(md)
    print(f"Wrote {args.output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
